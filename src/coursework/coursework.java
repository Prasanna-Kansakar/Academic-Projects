    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        try{
            String s=(String)jTable1.getValueAt(0,0);
            if(s != null && s.length() != 0){
                int Price = Integer.parseInt(jTextField6.getText().trim());
                ArrayList<Integer> a=new ArrayList();
                ArrayList<Integer> b=new ArrayList();
                for (int i=0;i<jTable1.getRowCount();i++){
                    a.add(Integer.parseInt((String)jTable1.getValueAt(i,3)));
                    b.add(Integer.parseInt((String)jTable1.getValueAt(i,0)));
                }
                int n = a.size();   
                for (int i = 0; i < n-1; i++) 
                { 
                    for (int j = i+1; j < n; j++) 
                        if (a.get(j) < a.get(i)){ 
                            int temp = a.get(j); 
                            a.set(j, a.get(i)); 
                            a.set(i, temp);
                            int temp1 = b.get(j); 
                            b.set(j, b.get(i)); 
                            b.set(i, temp1);                
                        }
                } 
                int low=0;
                int high = a.size()-1;
                boolean match= false;
                int mid;
                int find = Price;
                do{
                    mid= (low + high)/2;
                    if(a.get(mid)==find){
                        match= true;
                    }
                    else if(a.get(mid)>find){
                        high=mid-1;
                    }
                    else if(a.get(mid)<find){
                        low = mid +1;
                    }
                    if(low>high){
                        JOptionPane.showMessageDialog(jPanel2,"Price not found in the database");
                        break;
                    }
                }
                while(match==false);
                if(match==true){
                    int id= b.get(mid);
                    int num=0;
                    boolean match1= false;
                    do{
                        if(Integer.parseInt((String)jTable1.getValueAt(num,0))==id){
                           match1= true;
                        }
                        else{
                             num++;
                        }
                    }
                    while(match1==false);

                    JOptionPane.showMessageDialog(jPanel2,"Part Details: \n Id: "+(String)jTable1.getValueAt(num,0)+"\n Name: " + (String)jTable1.getValueAt(num,1)
                            +"\n Category: "+ (String)jTable1.getValueAt(num,2)+"\n Price: "+ (String)jTable1.getValueAt(num,3)+"\n Price Range: "+(String)jTable1.getValueAt(num,4)
                            +"\n Warrenty Period: "+ (String)jTable1.getValueAt(num,5)+"\n Discounts: "+(String)jTable1.getValueAt(num,6));
                }
                jTextField6.setText("");}
            else{
                JOptionPane.showMessageDialog(jPanel2,"No entries found in the database");
            } 
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(jPanel2,"Invalid Input! Write proper price in Integer format");
        }
    }//GEN-LAST:event_jButton6ActionPerformed
