        try{
            String Name = jTextField1.getText().trim();
            if(Name != null && Name.length() != 0){
                if (jTextField2.getText().trim()!= null && jTextField2.getText().length() != 0){
                    int Id = Integer.parseInt(jTextField2.getText().trim());
                    boolean match= false;
                    for(int j=0; j<jTable1.getRowCount(); j++){
                        String r = (String)jTable1.getValueAt(j,0);
                        if(r!= null && r.length() != 0 && r.equals(String.valueOf(Id))){
                            match = true;
                        }
                    }
                    if(match==false){
                        if (jTextField3.getText().trim()!= null && jTextField3.getText().length() != 0){
                        int Price = Integer.parseInt(jTextField3.getText().trim());
                        String Category; 
                        String Pricerange;
                        String Discount = "";
                        String Warrenty;
                        if(jSlider1.getValue()>1){
                             Warrenty= String.valueOf(jSlider1.getValue()) + " Years";
                        }
                        else{
                             Warrenty= String.valueOf(jSlider1.getValue()) + " Year";
                        }
                        if (jRadioButton1.isSelected() || jRadioButton2.isSelected()){
                             if(jRadioButton1.isSelected()){
                            Pricerange ="Low";
                            }
                            else {
                                 Pricerange ="High";
                            }
                            if (jComboBox1.getSelectedIndex()!=0){
                                Category = jComboBox1.getSelectedItem().toString().trim();
                                if (jCheckBox1.isSelected() || jCheckBox2.isSelected() || jCheckBox3.isSelected()){
                                    if (jCheckBox1.isSelected()){
                                        Discount = Discount + "10% Off on FonePay /";         
                                    }
                                    if (jCheckBox2.isSelected()){
                                        Discount = Discount + "15 % Off on Esewa /";
                                    }
                                    if (jCheckBox3.isSelected()){
                                        Discount = Discount + "3 % Off on Khalti /";
                                    }
                                         Discount = Discount.substring(0, Discount.length() - 1); 
                                    }
                                    else{
                                     Discount = "None";
                                    }
                                 int columnCount = jTable1.getColumnCount(); 
                                 int nextRow = 0;
                                 boolean emptyRow = false; 
                                 String s;
                                 String[] value = {String.valueOf(Id), Name, Category, String.valueOf(Price), Pricerange, Warrenty, Discount};
                                 do {
                                      s = (String)jTable1.getValueAt(nextRow,0);
                                      if (s != null && s.length() != 0){
                                        nextRow++;
                                      }
                                      else {
                                          emptyRow = true;
                                     }
                                     if (nextRow==jTable1.getRowCount())
                                     {
                                         DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
                                         model.addRow(new String[]{"", "", "", "", "", "", ""});
                                         emptyRow = true;
                                     }
                                 }
                                 while (!emptyRow);
                                 for (int i=0; i<columnCount; i++){
                                    jTable1.setValueAt(value[i],nextRow,i);
                                 }
                                 JOptionPane.showMessageDialog(jPanel1,"Item sucessfully Added"); 
                            }
                                else{
                                    JOptionPane.showMessageDialog(jPanel1,"Please Select a Category");
                                }
                            }
                            else {
                                JOptionPane.showMessageDialog(jPanel1,"Please Select a price range.");
                            }
                        }
                        else {
                            JOptionPane.showMessageDialog(jPanel1,"The value of Price cannot be Empty");
                        }   
                    }
                    else{
                        JOptionPane.showMessageDialog(jPanel1,"Id cannot be repeated"); 
                    }
                    
                }
                else{
                    JOptionPane.showMessageDialog(jPanel1,"The value of Id cannot be Empty"); 
                }
            }
            else{
                JOptionPane.showMessageDialog(jPanel1,"The value of Name cannot be Empty"); 
            }
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(jPanel1,"Invalid Input! Id and Price should be written in proper Integer format"); 
        }
